    private void btnAgregarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarRegistroActionPerformed
        double cantidad=0;
        String tipo;
        
        try{
            cantidad = Double.parseDouble(txtCantidad.getText());
            lblAdvertenciaCantidad.setVisible(false);
            tipo = (String) cmbTipoResiduo.getSelectedItem();
            DefaultTableModel model = (DefaultTableModel) tblRegistroResiduos.getModel();
            model.addRow(new Object[]{cantidad,tipo});
            txtCantidad.setText("0");
        }catch(NumberFormatException e){
            lblAdvertenciaCantidad.setVisible(true);
        }
        
    }//GEN-LAST:event_btnAgregarRegistroActionPerformed

